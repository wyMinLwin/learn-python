# Arithmetic Operators

friends = 10

print(f"friends = {friends}")
print(f"friends + 1 = { friends + 1 }")
print(f"friends - 1 = { friends - 1 }")
print(f"friends * 3 = { friends * 3 }")
print(f"friends / 3 = { friends / 3 }")
print(f"friends ** 2 = { friends ** 2 }")
print(f"friends % 3 = { friends % 3 }")
